    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTableAll.getModel();
        model.setRowCount(0);
        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (Employee emp : o.getEmployeeDirectory().getEmployeeList()) {
                Object[] row = new Object[2];
                row[0] = emp;
                row[1] = emp.getId();
                model.addRow(row);
            }
        }

    }

    private void populateTable2(Organization organization) {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();

        model.setRowCount(0);

        for (Employee employee : organization.getEmployeeDirectory().getEmployeeList()) {
            Object[] row = new Object[2];
            row[0] = employee;
            row[1] = employee.getId();
            model.addRow(row);
        }
    }

