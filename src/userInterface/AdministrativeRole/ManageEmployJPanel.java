    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int row = organizationJTableAll.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Employee e = (Employee) organizationJTableAll.getValueAt(row, 0);
        for (Organization o : organizationDirectory.getOrganizationList()) {
            o.getEmployeeDirectory().remove(e);
        }

        populateTable();
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        int row = organizationJTableAll.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Employee e = (Employee) organizationJTableAll.getValueAt(row, 0);

        txtViewName.setText(e.getName());
        txtViewAddress.setText(e.getAddress());
        txtViewContact.setText(e.getContactNo());
        Date doBirth = e.getDateOfBirth();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String stringDate = sdf.format(doBirth);
        txtViewDob.setText(stringDate);
        txtViewEmail.setText(e.geteMailId());
        txtViewGender.setText(e.getGender());
        txtViewId.setText(String.valueOf(e.getId()));

    }//GEN-LAST:event_btnViewDetailsActionPerformed

