    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        Organization organization = (Organization) organizationEmpJComboBox.getSelectedItem();
        String name = nameJTextField.getText();
        String address = txtAddress.getText();
        String emailId = txtemailId.getText();
        String contactNo = txtContactNumber.getText();
        Date d2 = dobChooser.getDate();
        String gend = (String) comboBoxGender.getSelectedItem();
        if (dobChooser.getDate() == null||organization==null || name.isEmpty()||address.isEmpty()||emailId.isEmpty()||contactNo.isEmpty()||gend.isEmpty()) {
            
            if(organization==null || name.isEmpty()||address.isEmpty()||emailId.isEmpty()||contactNo.isEmpty()||gend.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "You cannot leave any field empty");  
            }
            else
            {
                 JOptionPane.showMessageDialog(null, "Please select a date first"); 
            }
          
        } else {
            organization.getEmployeeDirectory().createEmployee(name, address, emailId, contactNo, d2, gend);
            populateTable();
            nameJTextField.setText("");
            txtAddress.setText("");
            txtemailId.setText("");
            txtContactNumber.setText("");
        }

    }//GEN-LAST:event_submitJButtonActionPerformed

