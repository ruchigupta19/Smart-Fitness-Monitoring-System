    private void btnMakeRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMakeRequestActionPerformed
        String message = messageJTextField.getText();
        TestVitalsWorkRequest request = new TestVitalsWorkRequest();
        request.setMessage(message);
        request.setPatientSender(userAccount);
        request.setStatus("sent to hospital for review");
        request.setVitalSignHistory(vitalHistory);
        request.setVitalSigns(vs);
//        request.setRequestDate();
        int flag = 0;
        for (WorkRequest request1 : userAccount.getWorkQueue().getWorkRequestList()) {
            if (String.valueOf(request1.getVitalSigns().getCurrentDate()).equals(String.valueOf(request.getVitalSigns().getCurrentDate()))) {

                flag = 1;
            }
        }

        if (flag == 0) {
            Organization org = null;
            for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (organization instanceof ReceptionistOrganization) {
                    org = organization;
                    break;
                }
            }
            if (org != null) {
                org.getWorkQueue().getWorkRequestList().add(request);
                userAccount.getWorkQueue().getWorkRequestList().add(request);
            }

            populateRequestTable();
            JOptionPane.showMessageDialog(null, "This request has been sent to the hospital to review");
        } else if (flag == 1) {
            JOptionPane.showMessageDialog(null, "This request has already been sent to the hospital");
        }
    }//GEN-LAST:event_btnMakeRequestActionPerformed

