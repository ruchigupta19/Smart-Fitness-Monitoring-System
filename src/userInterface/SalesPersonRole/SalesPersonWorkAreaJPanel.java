    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request : sOrganization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[7];
            row[0] = request;
            row[1] = request.getPatientSender().getEmployee().getName();
            row[2] = request.getDoctorSender().getEmployee().getName();
            row[3] = request.getSalesperson() == null ? null : request.getSalesperson().getEmployee().getName();
            row[4] = request.getStatus();
            row[5] = request.getVitalSigns();
            row[6] = request.getVitalSigns().getStatus();

            model.addRow(row);
        }
    }

