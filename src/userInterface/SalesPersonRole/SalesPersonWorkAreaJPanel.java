    private void btnMedicinesOrderedChkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMedicinesOrderedChkActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to view its order");
        } else {
            viewMedicines.setVisible(true);
            placeAnOrder.setVisible(true);
            WorkRequest request = (WorkRequest) workRequestJTable.getValueAt(selectedRow, 0);
            viewMedicines.setText(request.getMedicines());
        }

    }//GEN-LAST:event_btnMedicinesOrderedChkActionPerformed

    private void placeAnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_placeAnOrderActionPerformed
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to place an order");
        } else {
            TestVitalsWorkRequest request = (TestVitalsWorkRequest) workRequestJTable.getValueAt(selectedRow, 0);
            if (request.getSalesperson() != null && !request.getStatus().equals("Request has been sent to the Delivery person.")) {
                request.setStatus("Placing an order for medicines");
                userInterface.SalesPersonRole.PlaceOrderForPatient pofp = new userInterface.SalesPersonRole.PlaceOrderForPatient(userProcessContainer, userAccount, enterprise, request);
                userProcessContainer.add("pofp", pofp);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            } else if (request.getStatus().equals("Request has been sent to the Delivery person.")) {
                JOptionPane.showMessageDialog(null, "This request has already been processed and sent for delivery");
            } else {
                JOptionPane.showMessageDialog(null, "Assign the request first");
            }

        }

    }//GEN-LAST:event_placeAnOrderActionPerformed

    private void assignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignBtnActionPerformed
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "please select a request to be assigned");
        } else {
            WorkRequest request = (WorkRequest) workRequestJTable.getValueAt(selectedRow, 0);
            if (request.getSalesperson() != null) {
                JOptionPane.showMessageDialog(null, "This request has already been assigned to you");
            } else {
                request.setSalesperson(userAccount);
                request.setStatus("Pending with salesperson");
                populateTable();
            }
        }

    }//GEN-LAST:event_assignBtnActionPerformed

